/**
 * Инициализирует систему поиска для интерфейса
 * @param {string} searchField - Ключ поля поиска в состоянии приложения
 * @returns {Function} Функция для применения поисковых параметров к запросу
 */
export function initSearching(searchField) {
    /**
     * Применяет поисковые параметры к объекту запроса
     * @param {Object} query - Исходные параметры запроса
     * @param {Object} state - Текущее состояние приложения
     * @param {Object} action - Действие пользователя (опционально, не используется в текущей реализации)
     * @returns {Object} Обновленный объект запроса с поисковым параметром
     */
    return (query, state, action) => {
        // Проверяем наличие поискового запроса в состоянии приложения
        if (state[searchField]) {
            // Создаем новый объект запроса с добавленным поисковым параметром
            return Object.assign({}, query, {
                search: state[searchField]  // Добавляем поисковую строку в параметры запроса
            });
        } else {
            // Возвращаем исходный запрос без изменений, если поисковая строка пуста
            return query;
        }
    };
}